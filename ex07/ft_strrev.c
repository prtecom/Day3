char	ft_strrev(char *str)
{
	int l;
	int i;
	char cstr = *str;

	l = 0;
	i = 0;
	while (str[l] != '\0')
		l++;
	if (l == 1)
		return (str[l - 1]);
	else 
	{	
		while (i < l)
		{
			cstr[i] = str[l - 1];
			i++;
		}
		return(cstr);
	}
}
